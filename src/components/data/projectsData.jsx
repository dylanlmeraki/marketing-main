// Helper function to get project by slug
export const getProjectBySlug = (slug) => {
  return projects.find(project => project.slug === slug);
};

// Helper function to get unique categories
export const getUniqueCategories = () => {
  return [...new Set(projects.map(p => p.category))];
};

// Helper function to get unique services
export const getUniqueServices = () => {
  const allServices = projects.flatMap(p => p.services);
  return [...new Set(allServices)];
};

// Helper function to get unique counties
export const getUniqueCounties = () => {
  return [...new Set(projects.map(p => p.county))];
};